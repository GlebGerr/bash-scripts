#!/bin/bash

# Создание alias для команды ls -la
alias ll='ls -la'

# Печать сообщения об alias
echo "Alias ll создан для команды 'ls -la'."

# Инструкция для добавления alias в .bashrc, чтобы он был постоянным:
echo "Чтобы alias был постоянным, добавьте строку в файл .bashrc:"
echo "alias ll='ls -la'"

# Демонстрация автодополнения с командой cd:
