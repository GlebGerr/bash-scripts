#!/bin/bash

# Вывод текущего значения PATH
echo "Текущее значение PATH:"
echo "$PATH"

# Добавление новой директории в PATH
if [ -z "$1" ]; then
  echo "Пожалуйста, укажите директорию для добавления в PATH."
else
  export PATH="$PATH:$1"
  echo "Новая директория $1 добавлена в PATH."
  echo "Обновлённое значение PATH:"
  echo "$PATH"
fi

# Примечание: чтобы изменения были постоянными, добавьте команду в .bashrc
echo "Чтобы изменения стали постоянными, добавьте эту строку в .bashrc:"
echo "export PATH=\"\$PATH:$1\""
