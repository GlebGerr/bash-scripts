#!/bin/bash

# Создаёт список всех файлов в текущей директории с указанием типа
echo "Список всех файлов и директорий с указанием типа:"
for file in *; do
  if [ -d "$file" ]; then
    echo "$file - это директория"
  elif [ -f "$file" ]; then
    echo "$file - это файл"
  else
    echo "$file - это другой тип"
  fi
done

# Проверяет наличие файла, переданного как аргумент
if [ -z "$1" ]; then
  echo "Пожалуйста, укажите имя файла для проверки."
else
  if [ -e "$1" ]; then
    echo "Файл $1 существует."
  else
    echo "Файл $1 не найден."
  fi
fi

# Вывод имени и прав доступа для каждого файла
echo "Имя и права доступа для каждого файла:"
for file in *; do
  echo "$(basename "$file") - $(ls -l "$file" | awk '{print $1}')"
done
